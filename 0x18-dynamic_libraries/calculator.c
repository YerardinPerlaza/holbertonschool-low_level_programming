#include "calculator.h"

/**
 * add - Entry point
 * @a: int
 * @b: int
 * Return: Always 0 (Success)
 */

int add(int a, int b)
{
	int suma;

	suma = a + b;
	return (suma);
}

/**
 * sub - Entry point
 * @a: int
 * @b: int
 * Return: Always 0 (Success)
 */
int sub(int a, int b)
{
	int resta;

	resta = a - b;
	return (resta);
}
/**
 * mul - Entry point
 * @a: int
 * @b: int
 * Return: Always 0 (Success)
 */
int mul(int a, int b)
{
	int mul;

	mul = a * b;
	return (mul);
}
/**
 * div - Entry point
 * @a: int
 * @b: int
 * Return: Always 0 (Success)
 */
int div(int a, int b)
{
	int div;

	div = a / b;
	return (div);
}
/**
 * mod - Entry point
 * @a: int
 * @b: int
 * Return: Always 0 (Success)
 */
int mod(int a, int b)
{
	int modulo;

	modulo = a + b;
	return (modulo);
}
